#include <unistd.h>

char *ft_strcpy(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	while (s2[i] != '\0')
	{
		i++;
	}

	j = 0;
	while (j <= i)
	{
		s1[j] = s2[j];
		j++;
	}
	s1[j] = '\0';
return (s1);
}

